package in.shikhar.events;

public class ParticipantModel {
    String name;
    String year;
    String roll;
    String email;
    String phone;

    public ParticipantModel(String name, String year, String roll, String email, String phone) {
        this.name = name;
        this.year = year;
        this.roll = roll;
        this.email = email;
        this.phone = phone;
    }
}
